from __future__ import annotations

import json
import shutil
import tempfile
import unittest
from pathlib import Path

from featureliftbench.agent_runner import prepare_agent_workspace
from featureliftbench.evaluator import evaluate_submission
from featureliftbench.metadata import validate_metadata_shape
from featureliftbench.metrics import count_go_loc
from featureliftbench.validate import validate_task


class GoEvaluatorTests(unittest.TestCase):
    def test_validate_metadata_shape_accepts_go_metadata(self) -> None:
        metadata = _go_metadata("sample_go_task")

        errors = validate_metadata_shape(metadata)

        self.assertEqual(errors, [])

    def test_validate_task_requires_go_gate_files(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            task_dir = _make_go_task(Path(tmp) / "sample_go_task")
            (task_dir / "evaluation" / "allowed_modules.txt").unlink()

            result = validate_task(task_dir)

            self.assertFalse(result.valid)
            self.assertIn("missing required path: evaluation/allowed_modules.txt", result.errors)

    def test_count_go_loc_excludes_tests_vendor_and_generated(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            (root / "featurelifted").mkdir()
            (root / "featurelifted" / "adder.go").write_text(
                "package featurelifted\n\n"
                "// Add sums two ints.\n"
                "func Add(a int, b int) int {\n"
                "    return a + b\n"
                "}\n",
                encoding="utf-8",
            )
            (root / "featurelifted" / "adder_test.go").write_text("package featurelifted\n", encoding="utf-8")
            (root / "vendor" / "dep").mkdir(parents=True)
            (root / "vendor" / "dep" / "dep.go").write_text("package dep\nconst X = 1\n", encoding="utf-8")
            (root / "generated.go").write_text(
                "// Code generated by test. DO NOT EDIT.\n"
                "package featurelifted\n"
                "const Generated = 1\n",
                encoding="utf-8",
            )

            self.assertEqual(count_go_loc(root), 4)

    def test_evaluate_go_rejects_forbidden_import_without_go_toolchain(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            task_dir = _make_go_task(root / "sample_go_task")
            submission_dir = _make_go_submission(root / "submission")
            (submission_dir / "featurelifted" / "adder.go").write_text(
                "package featurelifted\n\n"
                "import _ \"example.com/featureliftbench/go-dummy-adder/adder\"\n\n"
                "func Add(a int, b int) int { return a + b }\n",
                encoding="utf-8",
            )

            result = evaluate_submission(task_dir, submission_dir, root / "output")

            self.assertEqual(result["status"], "failed")
            self.assertFalse(result["original_import_pass"])
            self.assertIn("imports forbidden Go package", "\n".join(result["errors"]))

    def test_evaluate_go_rejects_forbidden_module_and_bad_replace(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            task_dir = _make_go_task(root / "sample_go_task")
            submission_dir = _make_go_submission(root / "submission")
            (submission_dir / "go.mod").write_text(
                "module featurelifted.local/sample_go_task\n\n"
                "go 1.22\n\n"
                "require example.com/featureliftbench/go-dummy-adder v0.0.0\n"
                "replace example.com/featureliftbench/go-dummy-adder => ../repo\n",
                encoding="utf-8",
            )

            result = evaluate_submission(task_dir, submission_dir, root / "output")

            self.assertEqual(result["status"], "failed")
            self.assertFalse(result["forbidden_module_pass"])
            errors = "\n".join(result["errors"])
            self.assertIn("requires forbidden Go module", errors)
            self.assertIn("replace target must not point", errors)

    def test_evaluate_go_rejects_go_work(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            task_dir = _make_go_task(root / "sample_go_task")
            submission_dir = _make_go_submission(root / "submission")
            (submission_dir / "go.work").write_text("go 1.22\n", encoding="utf-8")

            result = evaluate_submission(task_dir, submission_dir, root / "output")

            self.assertEqual(result["status"], "failed")
            self.assertIn("go.work is not allowed", "\n".join(result["errors"]))

    def test_prepare_go_agent_workspace_uses_go_prompt_and_public_runner(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            task_dir = _make_go_task(root / "sample_go_task")
            metadata = json.loads((task_dir / "metadata.json").read_text(encoding="utf-8"))
            workspace_dir = root / "workspace"

            task_file = prepare_agent_workspace(task_dir, workspace_dir, metadata)

            prompt = task_file.read_text(encoding="utf-8")
            self.assertIn("FeatureLiftBench Go Task", prompt)
            self.assertIn("submission/go.mod", prompt)
            self.assertIn("run_public_tests.sh", prompt)
            self.assertNotIn("installable Python package", prompt)
            self.assertTrue((workspace_dir / "run_public_tests.sh").is_file())
            self.assertFalse((workspace_dir / "requirements.lock").exists())

    @unittest.skipUnless(shutil.which("go"), "go toolchain is not installed")
    def test_evaluate_go_submission_passes_minimal_task(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            task_dir = _make_go_task(root / "sample_go_task")
            submission_dir = _make_go_submission(root / "submission")

            result = evaluate_submission(task_dir, submission_dir, root / "output")

            self.assertEqual(result["status"], "passed", result["errors"])
            self.assertTrue(result["build_pass"])
            self.assertTrue(result["test_pass"])
            self.assertTrue(result["offline_dependency_pass"])
            self.assertEqual(result["scores"]["functional_gate"], 1.0)

    @unittest.skipUnless(shutil.which("go"), "go toolchain is not installed")
    def test_evaluate_go_public_pass_hidden_fail(self) -> None:
        with tempfile.TemporaryDirectory() as tmp:
            root = Path(tmp)
            task_dir = _make_go_task(root / "sample_go_task")
            submission_dir = _make_go_submission(root / "submission")
            (submission_dir / "featurelifted" / "adder.go").write_text(
                "package featurelifted\n\n"
                "func Add(a int, b int) int {\n"
                "    if a < 0 || b < 0 { return 0 }\n"
                "    return a + b\n"
                "}\n",
                encoding="utf-8",
            )

            result = evaluate_submission(task_dir, submission_dir, root / "output")

            self.assertEqual(result["status"], "failed")
            self.assertTrue(result["public_tests"]["passed"])
            self.assertFalse(result["hidden_tests"]["passed"])


def _make_go_task(task_dir: Path) -> Path:
    task_dir.mkdir(parents=True)
    (task_dir / "repo" / "adder").mkdir(parents=True)
    (task_dir / "public_tests").mkdir()
    (task_dir / "hidden_tests").mkdir()
    (task_dir / "evaluation").mkdir()
    metadata = _go_metadata(task_dir.name)
    (task_dir / "metadata.json").write_text(json.dumps(metadata), encoding="utf-8")
    (task_dir / "repo" / "go.mod").write_text(
        "module example.com/featureliftbench/go-dummy-adder\n\ngo 1.22\n",
        encoding="utf-8",
    )
    (task_dir / "repo" / "adder" / "adder.go").write_text(
        "package adder\n\nfunc Add(a int, b int) int { return a + b }\n",
        encoding="utf-8",
    )
    (task_dir / "public_tests" / "adder_public_test.go").write_text(
        f"package featurelifted_test\n\n"
        f"import (\n"
        f"    \"testing\"\n"
        f"    flb \"featurelifted.local/{task_dir.name}/featurelifted\"\n"
        f")\n\n"
        f"func TestAddPositive(t *testing.T) {{\n"
        f"    if got := flb.Add(2, 3); got != 5 {{ t.Fatalf(\"got %d\", got) }}\n"
        f"}}\n",
        encoding="utf-8",
    )
    (task_dir / "hidden_tests" / "adder_hidden_test.go").write_text(
        f"package featurelifted_test\n\n"
        f"import (\n"
        f"    \"testing\"\n"
        f"    flb \"featurelifted.local/{task_dir.name}/featurelifted\"\n"
        f")\n\n"
        f"func TestAddNegative(t *testing.T) {{\n"
        f"    if got := flb.Add(-4, 7); got != 3 {{ t.Fatalf(\"got %d\", got) }}\n"
        f"}}\n",
        encoding="utf-8",
    )
    (task_dir / "evaluation" / "forbidden_imports.txt").write_text(
        "example.com/featureliftbench/go-dummy-adder\n",
        encoding="utf-8",
    )
    (task_dir / "evaluation" / "forbidden_modules.txt").write_text(
        "example.com/featureliftbench/go-dummy-adder\n",
        encoding="utf-8",
    )
    (task_dir / "evaluation" / "allowed_modules.txt").write_text("", encoding="utf-8")
    (task_dir / "evaluation" / "oracle_manifest.json").write_text("{}", encoding="utf-8")
    return task_dir


def _make_go_submission(submission_dir: Path) -> Path:
    submission_dir.mkdir(parents=True)
    (submission_dir / "featurelifted").mkdir()
    task_id = "sample_go_task"
    (submission_dir / "go.mod").write_text(
        f"module featurelifted.local/{task_id}\n\ngo 1.22\n",
        encoding="utf-8",
    )
    (submission_dir / "featurelifted" / "adder.go").write_text(
        "package featurelifted\n\nfunc Add(a int, b int) int { return a + b }\n",
        encoding="utf-8",
    )
    return submission_dir


